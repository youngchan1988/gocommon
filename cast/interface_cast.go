func InterfaceToMapInterface(i interface{}) (map[string]interface{}, error) {
	var m = map[string]interface{}{}

	switch v := i.(type) {
	case map[interface{}]interface{}:
		for k, val := range v {
			m[InterfaceToStringWithDefault(k)] = val
		}
		return m, nil
	case map[string]interface{}:
		return v, nil
	case string:
		err := jsonStringToObject(v, &m)
		return m, err
	default:
		buf, err := json.Marshal(v)
		if err != nil {
			return m, fmt.Errorf("unable to cast %#v of type %T to map[string]interface{}: %v", i, i, err)
		}
		err = json.Unmarshal(buf, &m)
		return m, err
	}
}

