func InterfaceToMapStringSlice(i interface{}) (map[string][]string, error) {
	var m = map[string][]string{}

	switch v := i.(type) {
	case map[string][]string:
		return v, nil
	case map[string][]interface{}:
		for k, val := range v {
			m[InterfaceToStringWithDefault(k)] = InterfaceToSliceStringWithDefault(val)
		}
		return m, nil
	case map[string]string:
		for k, val := range v {
			m[InterfaceToStringWithDefault(k)] = []string{val}
		}
	case map[string]interface{}:
		for k, val := range v {
			switch vt := val.(type) {
			case []interface{}:
				m[InterfaceToStringWithDefault(k)] = InterfaceToSliceStringWithDefault(vt)
			case []string:
				m[InterfaceToStringWithDefault(k)] = vt
			default:
				m[InterfaceToStringWithDefault(k)] = []string{InterfaceToStringWithDefault(val)}
			}
		}
		return m, nil
	case map[interface{}][]string:
		for k, val := range v {
			m[InterfaceToStringWithDefault(k)] = InterfaceToSliceStringWithDefault(val)
		}
		return m, nil
	case map[interface{}]string:
		for k, val := range v {
			m[InterfaceToStringWithDefault(k)] = InterfaceToSliceStringWithDefault(val)
		}
		return m, nil
	case map[interface{}][]interface{}:
		for k, val := range v {
			m[InterfaceToStringWithDefault(k)] = InterfaceToSliceStringWithDefault(val)
		}
		return m, nil
	case map[interface{}]interface{}:
		for k, val := range v {
			key, err := InterfaceToString(k)
			if err != nil {
				return m, fmt.Errorf("unable to cast %#v of type %T to map[string][]string", i, i)
			}
			value, err := InterfaceToSliceString(val)
			if err != nil {
				return m, fmt.Errorf("unable to cast %#v of type %T to map[string][]string", i, i)
			}
			m[key] = value
		}
	case string:
		err := jsonStringToObject(v, &m)
		return m, err
	default:
		return m, fmt.Errorf("unable to cast %#v of type %T to map[string][]string", i, i)
	}
	return m, nil
}

