var errNegativeNotAllowed = errors.New("unable to cast negative value")

func InterfaceToBoolWithDefault(i interface{}, defaultValue ...bool) bool {
	v, err := InterfaceToBool(i)
	if len(defaultValue) > 0 && (i == nil || err != nil) {
		return defaultValue[0]
	}
	return v
}

func InterfaceToBool(i interface{}) (bool, error) {
	i = indirect(i)

	switch b := i.(type) {
	case bool:
		return b, nil
	case nil:
		return false, nil
	case int:
		if i.(int) == 0 {
			return false, nil
		}
		return true, nil
	case string:
		return strconv.ParseBool(i.(string))
	default:
		return false, fmt.Errorf("unable to cast %#v of type %T to bool", i, i)
	}
}

